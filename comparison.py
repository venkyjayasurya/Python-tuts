a,b=10,20

print("%d equal to %d = %d "%(a,b,a==b))

print("%d not equal to %d = %d "%(a,b,a!=b))

print("%d is greater than %d = %d "%(a,b,a>b))

print("%d is Less than %d = %d "%(a,b,a<b))

print("%d is greater than or equal to %d = %d "%(a,b,a>=b))

print("%d is Less than or equal to %d = %d "%(a,b,a<=b))